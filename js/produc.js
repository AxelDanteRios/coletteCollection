function siguientePagina() {
    const prodList = filtroActual
        ? productosColette.filter(producto => producto.categoria === filtroActual)
        : productosColette;

    // Verifica si la página actual tiene productos
    const totalPaginas = Math.ceil(prodList.length / elementosPorPagina);
    if (paginaActual < totalPaginas) {
        btnSiguiente.disabled
        paginaActual++;
        displayproducto(prodList, paginaActual);
    }
}

function paginaAnterior() {
    if (paginaActual > 1) {
        paginaActual--;
        const prodList = filtroActual
            ? productosColette.filter(producto => producto.categoria === filtroActual)
            : productosColette;
        displayproducto(prodList, paginaActual);
    }
}



